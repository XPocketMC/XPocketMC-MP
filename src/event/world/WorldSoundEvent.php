declare(strict_types=1);

namespace xpocketmc\event\world;

use xpocketmc\event\Cancellable;
use xpocketmc\event\CancellableTrait;
use pocketmine\math\Vector3;
use xpocketmc\player\Player;
use xpocketmc\world\sound\Sound;
use xpocketmc\world\World;

/**
 * Called when a sound is played in a world
 * @see World::addSound()
 */
class WorldSoundEvent extends WorldEvent implements Cancellable{
	use CancellableTrait;

	/**
	 * @param Player[] $recipients
	 */
	public function __construct(
		World $world,
		private Sound $sound,
		private Vector3 $position,
		private array $recipients
	){
		parent::__construct($world);
	}

	public function getSound() : Sound{
		return $this->sound;
	}

	public function setSound(Sound $sound) : void{
		$this->sound = $sound;
	}

	public function getPosition() : Vector3{
		return $this->position;
	}

	/**
	 * @return Player[]
	 */
	public function getRecipients() : array{
		return $this->recipients;
	}

	/**
	 * @param Player[] $recipients
	 */
	public function setRecipients(array $recipients) : void{
		$this->recipients = $recipients;
	}
}